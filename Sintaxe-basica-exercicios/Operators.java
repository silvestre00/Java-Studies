public class Operators {
    public static void main(String[] args){
        int num1 = 11;
        int num2 = 7;

        System.out.println("Logical Operators: ");
        System.out.println("The sum of number 1 and number 2 is: " + (num1 + num2));
        System.out.println("The subtraction between the number 1 and the number 2 is: " + (num1 - num2));
        System.out.println("The multiplication between the number 1 and the number 2 is: " + (num1 * num2));
        System.out.println("The division between the number 1 and the number 2 is: " + (num1 / num2));
        System.out.println("The remainder of division between the number 1 and the number 2 is: " + (num1 % num2));
        System.out.println("Relational Operators: ");
        System.out.println("Is the number 1 the same as the number 2? " + (num1 == num2));
        System.out.println("Is number 1 different from number 2? " + (num1 != num2));
        System.out.println("Is number 1 greater than number 2? " + (num1 > num2));
        System.out.println("Is the number 1 greater than or equal to the number 2? " + (num1 >= num2));
        System.out.println("Is number 1 smaller than number 2? " + (num1 < num2));
        System.out.println("Is the number 1 less than or equal to the number 2? " + (num1 <= num2));
    }
}
